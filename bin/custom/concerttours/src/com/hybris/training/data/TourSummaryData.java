package com.hybris.training.data;

public class TourSummaryData
{
    private String id;
    private String tourName;
    private String numberOfConcerts;

    public String getId()
    {
        return id;
    }

    public void setId(String id)
    {
        this.id = id;
    }

    public String getTourName()
    {
        return tourName;
    }

    public void setTourName(String tourName)
    {
        this.tourName = tourName;
    }

    public String getNumberOfConcerts()
    {
        return numberOfConcerts;
    }

    public void setNumberOfConcerts(String numberOfConcerts)
    {
        this.numberOfConcerts = numberOfConcerts;
    }
}
